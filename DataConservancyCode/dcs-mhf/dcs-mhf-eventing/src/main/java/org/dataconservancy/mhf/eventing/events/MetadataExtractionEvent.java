package org.dataconservancy.mhf.eventing.events;

public class MetadataExtractionEvent extends MetadataHandlingEvent {

    public static enum ExtractionEventType {
        EXTRACTION,
        ERROR
    };
    
    private ExtractionEventType type;
    
    //TODO: Fill in extraction event specific details
    /**
     * @param objectId
     * @param message
     */
    public MetadataExtractionEvent(String objectId, String message, ExtractionEventType type) {
        super(objectId, message);
        
        this.type = type;
    }
    
    public ExtractionEventType getType() {
        return type;
    }
    
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((objectId == null) ? 0 : objectId.hashCode());
        result = prime * result + ((message == null) ? 0 : message.hashCode());
        result = prime * result + ((type == null) ? 0 : type.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        MetadataExtractionEvent other = (MetadataExtractionEvent) obj;
        if (objectId == null) {
            if (other.objectId != null)
                return false;
        } else if (!objectId.equals(other.objectId))
            return false;
        if (message == null) {
            if (other.message != null)
                return false;
        } else if (!message.equals(other.message))
            return false;
        if (type == null) {
            if (other.type != null)
                return false;
        } else if (!type.equals(other.type))
            return false;
        
        return true;
    }
}